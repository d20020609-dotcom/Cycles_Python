# 8. ПРАКТИЧЕСКИЕ ЗАДАЧИ
# ==================================================

print("\n--- 8. Практические примеры ---\n")

# Пример 8.1: факториал числа
n = 5
factorial = 1
for i in range(1, n + 1):
    factorial *= i
print(f"Факториал числа {n} = {factorial}")

# Пример 8.2: числа Фибоначчи
print("\nЧисла Фибоначчи (первые 10):")
a, b = 0, 1
for i in range(10):
    print(f"  {a}", end=" ")
    a, b = b, a + b
print()

# Пример 8.3: проверка простого числа
num = 17
is_prime = True
if num < 2:
    is_prime = False
else:
    for i in range(2, int(num ** 0.5) + 1):
        if num % i == 0:
            is_prime = False
            break
print(f"\nЧисло {num} простое? {is_prime}")

# ==================================================
# 9. ГЕНЕРАТОРЫ СПИСКОВ (List Comprehensions)
# ==================================================

print("\n--- 9. Генераторы списков ---\n")

# Пример 9.1: квадраты чисел
squares = [x ** 2 for x in range(1, 6)]
print(f"Квадраты чисел от 1 до 5: {squares}")

# Пример 9.2: четные числа
evens = [x for x in range(1, 11) if x % 2 == 0]
print(f"Четные числа от 1 до 10: {evens}")

# Пример 9.3: преобразование строк
names = ["анна", "борис", "вика"]
capitalized = [name.title() for name in names]
print(f"Имена с большой буквы: {capitalized}")

