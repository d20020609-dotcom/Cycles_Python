# ==================================================
# 1. ЦИКЛ FOR С range()
# ==================================================

print("\n--- 1. Цикл for с range() ---\n")

# Пример 1.1: range(stop) - от 0 до 4
print("Числа от 0 до 4:")
for i in range(5):
    print(f"  {i}")

# Пример 1.2: range(start, stop) - от 1 до 5
print("\nЧисла от 1 до 5:")
for i in range(1, 6):
    print(f"  {i}")

# Пример 1.3: range(start, stop, step) - с шагом 2
print("\nЧетные числа от 2 до 10:")
for i in range(2, 11, 2):
    print(f"  {i}")

# Пример 1.4: обратный отсчет
print("\nОбратный отсчет от 5 до 1:")
for i in range(5, 0, -1):
    print(f"  {i}")

# ==================================================
# 2. ЦИКЛ FOR ДЛЯ ПЕРЕБОРА СПИСКОВ
# ==================================================

print("\n--- 2. Цикл for для перебора списков ---\n")

# Пример 2.1: список учеников
students = ["Анна", "Борис", "Вика", "Глеб", "Дима"]
print("Список учеников:")
for student in students:
    print(f"  {student} учится в 10 классе")

# Пример 2.2: список чисел и вычисления
numbers = [10, 20, 30, 40, 50]
print("\nКвадраты чисел:")
for num in numbers:
    print(f"  {num} в квадрате = {num * num}")

# Пример 2.3: список с разными типами
mixed = [42, "Python", 3.14, True]
print("\nЭлементы списка с разными типами:")
for item in mixed:
    print(f"  {item} -> тип: {type(item).__name__}")

# ==================================================
# 3. ЦИКЛ FOR ДЛЯ ПЕРЕБОРА СТРОК
# ==================================================

print("\n--- 3. Цикл for для перебора строк ---\n")

# Пример 3.1: перебор символов
word = "Python"
print(f"Символы слова '{word}':")
for letter in word:
    print(f"  {letter}")

# Пример 3.2: подсчет гласных
text = "Программирование"
vowels = "аеёиоуыэюяАЕЁИОУЫЭЮЯ"
count = 0
print(f"\nПодсчет гласных в слове '{text}':")
for char in text:
    if char in vowels:
        count += 1
        print(f"  Найдена гласная: {char}")
print(f"  Всего гласных: {count}")

# ==================================================
# 4. ЦИКЛ WHILE
# ==================================================

print("\n--- 4. Цикл while ---\n")

# Пример 4.1: счетчик
count = 1
print("Счет от 1 до 5:")
while count <= 5:
    print(f"  {count}")
    count += 1

# Пример 4.2: сумма чисел
print("\nСумма чисел от 1 до 10:")
num = 1
total = 0
while num <= 10:
    total += num
    print(f"  Добавили {num}, сумма стала: {total}")
    num += 1
print(f"  Итоговая сумма: {total}")

# Пример 4.3: пока пользователь не введет правильный ответ
print("\nУгадай число (от 1 до 5):")
secret = 3
guess = 0
while guess != secret:
    guess = int(input("  Введи число: "))
    if guess < secret:
        print("    Загаданное число больше!")
    elif guess > secret:
        print("    Загаданное число меньше!")
print("    Поздравляю! Ты угадал!")

# ==================================================
# 5. ВЛОЖЕННЫЕ ЦИКЛЫ
# ==================================================

print("\n--- 5. Вложенные циклы ---\n")

# Пример 5.1: таблица умножения
print("Таблица умножения 2x2:")
for i in range(1, 4):
    for j in range(1, 4):
        print(f"  {i} × {j} = {i * j}")
    print()  # пустая строка после каждого ряда

# Пример 5.2: прямоугольник из звездочек
print("Прямоугольник 5x3 из звездочек:")
for i in range(3):
    for j in range(5):
        print("*", end=" ")
    print()

# ==================================================
# 6. BREAK И CONTINUE
# ==================================================

print("\n--- 6. break и continue ---\n")

# Пример 6.1: break - прерывание цикла
print("break - ищем число 7:")
for i in range(1, 11):
    if i == 7:
        print(f"  Нашли число {i}, цикл прерван!")
        break
    print(f"  Проверили: {i}")

# Пример 6.2: continue - пропуск итерации
print("\ncontinue - пропускаем четные числа:")
for i in range(1, 11):
    if i % 2 == 0:
        print(f"  {i} - четное, пропускаем")
        continue
    print(f"  {i} - нечетное, выводим")

# ==================================================
# 7. ELSE В ЦИКЛАХ
# ==================================================

print("\n--- 7. else в циклах ---\n")

# Пример 7.1: else после for
print("else после for (цикл завершился нормально):")
for i in range(5):
    print(f"  Итерация {i}")
else:
    print("  Цикл завершился без break!")

# Пример 7.2: else не выполняется после break
print("\nelse после for (цикл прерван):")
for i in range(5):
    if i == 3:
        print("  break! Цикл прерван")
        break
    print(f"  Итерация {i}")
else:
    print("  Это не выполнится")

